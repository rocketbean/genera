#!/usr/bin/env node
/**
 * Type-checks every ` ```ts ` code block in the README files so a developer can
 * copy-paste a snippet and have it compile. Catches nonexistent imports, wrong
 * method names, and undeclared identifiers (the "refresh_token" class of bug).
 *
 * Each block is compiled as its own module against the packages' built `.d.ts`
 * (so this must run AFTER `pnpm -r build`). Conventional "your instance"
 * placeholders (`storage`, `driver`, …) are declared ambiently so fragment
 * snippets that operate on an established instance still check; anything else
 * undeclared is a real error.
 *
 * Opt a block out by tagging its fence: ` ```ts skip ` (for illustrative
 * signatures / incomplete skeletons). Run: `node scripts/check-readme-snippets.mjs`.
 */
import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readdirSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const repoRoot = process.cwd();
const tmpDir = join(repoRoot, "node_modules", ".cache", "genera-doc-check");

/** README files to check (the npm-facing docs). */
const files = [
  "README.md",
  ...readdirSync("packages")
    .map((d) => join("packages", d, "README.md"))
    .filter((p) => existsSync(p)),
];

/** Extract fenced ts/typescript blocks, honoring a `skip` fence tag. */
function extractBlocks(file) {
  const lines = readFileSync(file, "utf8").split("\n");
  const blocks = [];
  let open = null;
  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    if (open) {
      if (line.trim() === "```") {
        blocks.push({ file, startLine: open.startLine, code: open.code.join("\n") });
        open = null;
      } else {
        open.code.push(line);
      }
      continue;
    }
    const m = /^```(\S*)\s*(.*)$/.exec(line);
    if (!m) continue;
    const lang = m[1].toLowerCase();
    const info = m[2].toLowerCase();
    if ((lang === "ts" || lang === "typescript") && !/\b(skip|no-check)\b/.test(info)) {
      open = { startLine: i + 1, code: [] };
    } else if (line.startsWith("```")) {
      // a non-ts (or skipped) fence — consume to its close so we don't mis-scan
      for (i++; i < lines.length && lines[i].trim() !== "```"; i++);
    }
  }
  return blocks;
}

const blocks = files.flatMap(extractBlocks);
if (process.env.GENERA_DOC_DEBUG) {
  blocks.forEach((b, i) => console.error(`snippet-${i}  ${b.file}:${b.startLine}`));
}
if (blocks.length === 0) {
  console.error("No ts code blocks found — did the README paths change?");
  process.exit(1);
}

// Fresh temp workspace.
rmSync(tmpDir, { recursive: true, force: true });
mkdirSync(tmpDir, { recursive: true });

// An ambient .d.ts (no import/export) so `declare module` and the global
// placeholders apply across every snippet file.
const prelude = `/* Generated by scripts/check-readme-snippets.mjs — do not edit. */
// SDKs snippets import directly but that aren't installed at the repo root.
declare module "google-auth-library";
declare module "box-node-sdk";

// Conventional "your instance" placeholders used in fragment snippets.
declare const storage: any;
declare const driver: any;
declare const sourceDriver: any;
declare const destDriver: any;
declare const oauthProvider: any;
declare const someReadableStream: any;
declare const metrics: any;
declare const logger: any;
// Node's process.env (snippets use it; @types/node isn't resolvable here).
declare const process: { env: Record<string, string | undefined> };
`;
writeFileSync(join(tmpDir, "_prelude.d.ts"), prelude);

const indexToBlock = new Map();
blocks.forEach((b, i) => {
  const name = `snippet-${i}.ts`;
  indexToBlock.set(name, b);
  // Append `export {}` so each file is a module (enables top-level await + global shadowing).
  writeFileSync(join(tmpDir, name), `${b.code}\nexport {};\n`);
});

const pkgDir = (p) => join(repoRoot, "packages", p, "dist");
const tsconfig = {
  compilerOptions: {
    target: "es2022",
    module: "esnext",
    moduleResolution: "bundler",
    lib: ["es2022", "dom", "dom.iterable"],
    types: [],
    strict: true,
    skipLibCheck: true,
    noEmit: true,
    esModuleInterop: true,
    forceConsistentCasingInFileNames: true,
    // No `baseUrl` (deprecated in TS6) — the path targets below are absolute.
    paths: {
      "@rocketbean/genera": [join(pkgDir("genera"), "index.d.ts")],
      "@rocketbean/genera/node": [join(pkgDir("genera"), "node.d.ts")],
      "@rocketbean/genera/conformance": [join(pkgDir("genera"), "conformance.d.ts")],
      "@rocketbean/genera-s3": [join(pkgDir("genera-s3"), "index.d.ts")],
      "@rocketbean/genera-gcs": [join(pkgDir("genera-gcs"), "index.d.ts")],
      "@rocketbean/genera-azure": [join(pkgDir("genera-azure"), "index.d.ts")],
      "@rocketbean/genera-dropbox": [join(pkgDir("genera-dropbox"), "index.d.ts")],
      "@rocketbean/genera-onedrive": [join(pkgDir("genera-onedrive"), "index.d.ts")],
      "@rocketbean/genera-gdrive": [join(pkgDir("genera-gdrive"), "index.d.ts")],
      "@rocketbean/genera-box": [join(pkgDir("genera-box"), "index.d.ts")],
    },
  },
  include: ["_prelude.d.ts", "snippet-*.ts"],
};
writeFileSync(join(tmpDir, "tsconfig.json"), JSON.stringify(tsconfig, null, 2));

const tsc = join(repoRoot, "node_modules", ".bin", "tsc");
let output = "";
let failed = false;
try {
  execFileSync(tsc, ["-p", join(tmpDir, "tsconfig.json")], { encoding: "utf8", stdio: "pipe" });
} catch (err) {
  failed = true;
  output = `${err.stdout ?? ""}${err.stderr ?? ""}`;
}

if (!failed) {
  console.log(`✓ ${blocks.length} README ts snippets type-check.`);
  rmSync(tmpDir, { recursive: true, force: true });
  process.exit(0);
}

// Map `snippet-<i>.ts(line,col): error …` back to the source README + block.
const errorsByBlock = new Map();
for (const raw of output.split("\n")) {
  const m = /snippet-(\d+)\.ts\((\d+),(\d+)\):\s*(.*)$/.exec(raw.trim());
  if (!m) continue;
  const name = `snippet-${m[1]}.ts`;
  const block = indexToBlock.get(name);
  if (!block) continue;
  const list = errorsByBlock.get(name) ?? [];
  list.push({ line: Number(m[2]), col: Number(m[3]), msg: m[4] });
  errorsByBlock.set(name, list);
}

console.error("✗ README snippet type-check failed:\n");
if (errorsByBlock.size === 0) {
  console.error("(could not map tsc output to blocks — raw output follows)\n");
  console.error(output);
}
for (const [name, errs] of errorsByBlock) {
  const block = indexToBlock.get(name);
  console.error(`  ${block.file}  (block starting at line ${block.startLine}):`);
  for (const e of errs) {
    console.error(`    L${e.line}:${e.col}  ${e.msg}`);
  }
  console.error("");
}
console.error(
  "Fix the snippet (add the missing import / declare the value), or tag an\n" +
    "illustrative block's fence with ` ```ts skip ` if it isn't meant to compile.",
);
rmSync(tmpDir, { recursive: true, force: true });
process.exit(1);
